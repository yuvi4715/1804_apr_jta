//Write a program to add, subtract, multiply and divide two non-zero hard-coded numbers.

package CoreJava;

public class question1 {

	public static void main(String[] args) {
		double x = 5;
		double y = 10;
		System.out.println("The addition output is: " + (x + y));
		System.out.println("The subtraction output is: " + (x - y));
		System.out.println("The multiplication output is: " + (x * y));
		System.out.println("The division output is: " + (x / y));
	}
		
	
	
}
