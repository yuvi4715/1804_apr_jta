package question53;

public class Dup {
	private int num;
	private boolean bol;
	public Dup(int num, boolean bol) {
		super();
		this.num = num;
		this.bol = bol;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public boolean isBol() {
		return bol;
	}
	public void setBol(boolean bol) {
		this.bol = bol;
	}
}
