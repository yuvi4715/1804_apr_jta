package bonusExercises;

public class Problem55 {
	
	public static void main(String[] args) {
		
		
		System.out.println(isEven(0));
		
		
	}
	
	public static boolean isEven(int k) {
		
		int kHalf = k/2;
		
		if (k == kHalf*2) {
			
			return true;
			
		} else {
		
		return false;
		
		}
		
		
	}
	
	
	
	
	
	

}
