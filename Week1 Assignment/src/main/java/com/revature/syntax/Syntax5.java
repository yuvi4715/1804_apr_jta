package com.revature.syntax;

public class Syntax5 {
	/*
	 * 5.In one project, create two classes. One class should contain only methods
	 * (add, subtract, multiply and divide). The other class should contain only the
	 * main() method which calls each of the methods from the previous class.
	 */
	void Add(int x, int y) {
		int adding = x + y;
		System.out.println(adding);

	}

	void Subtract(int x, int y) {
		int subtraction = x - y;
		System.out.println(subtraction);
	}

	void Multiply(int x, int y) {
		int multiplying = x * y;
		System.out.println(multiplying);
	}

	void Divide(int x, int y) {
		int dividing = x / y;
		System.out.println(dividing);
	}
}
